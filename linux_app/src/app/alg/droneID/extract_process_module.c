/* calculate mean of window data */
Cplxf_t CalcMean(Cplxf_t *winBuff, uint32_t length)
{
	int32_t ii = 0;
	Cplxf_t acc = {0.0f};
	Cplxf_t mean = {0.0f};
	float inverseWinSize = (1.0/(float)length);

	for (ii = 0; ii < length; ii++)
	{
		acc.re += winBuff[ii].re;
		acc.im += winBuff[ii].im;
	}

	mean.re = acc.re * inverseWinSize;
	mean.im = acc.im * inverseWinSize;

	return mean;
}


/* calculate std. */
float CalcStd(Cplxf_t *x, Cplxf_t mean, uint32_t length, Cplxf_t *x0)
{
	int32_t ii = 0;
	float acc = 0.0;
	float std = 0.0;

	for (ii = 0; ii < length; ii++)
	{
		/* substract x from mean of x */
		x0[ii].re = x[ii].re - mean.re;
		x0[ii].im = x[ii].im - mean.im;

		acc +=   x0[ii].re*x0[ii].re  + x0[ii].im*x0[ii].im;
	}

	std = sqrtf(acc/(length-1));

	return std;
}

